#[derive(Debug)]
struct Reactant {
    name: String,
    count: usize,
}

#[derive(Debug)]
struct Reaction {
    produced: usize,
    reactants: Vec<Reactant>,
}

type TInput = HashMap<String, Reaction>;

fn parse_reactant(input: &str) -> Reactant {
    let (count, name) = input.split_once(" ").unwrap();
    let count: usize = count.parse().unwrap();
    let name = name.to_string();

    Reactant { name, count }
}

fn parse(input: &str) -> TInput {
    input
        .lines()
        .map(|l| {
            let (reactants, product) = l.split_once(" => ").unwrap();
            let product = parse_reactant(product);
            let reactants: Vec<Reactant> = reactants.split(", ").map(parse_reactant).collect();
            (product.name, Reaction { produced: product.count, reactants: reactants })
        }).collect()
}

fn run(reactions: &TInput, fuel: usize) -> usize {
    let mut excess: HashMap<String, usize> = HashMap::new();
    let mut to_expand = vec![(fuel, String::from("FUEL"))];
    let mut ore = 0;

    while let Some((mut needed, chemical)) = to_expand.pop() {
        if chemical == "ORE" {
            ore += needed;
            continue;
        }

        let reaction = reactions.get(&chemical).unwrap();
        let excess = excess.entry(chemical).or_insert(0);

        if *excess >= needed {
            *excess -= needed;
            continue;
        } else {
            needed -= *excess;
            *excess = 0;
        }
       
        let runs = needed.div_ceil(reaction.produced);
        let produced = reaction.produced * runs;
        if produced > needed {
            *excess += produced - needed;
        }

        for reactant in &reaction.reactants {
            to_expand.push((reactant.count * runs, reactant.name.clone()));
        }
    }

    ore
}

fn part1(reactions: &TInput) -> usize {
    run(reactions, 1)
}

fn part2(reactions: &TInput) -> usize {
    let trillion = 1_000_000_000_000;
    let ore_per_fuel = run(reactions, 1);

    let mut min = trillion / ore_per_fuel;
    let mut max = 5 * min;

    while max > min {
        let mut mid = (min + max) / 2;
        let needed = run(reactions, mid);
        if needed > trillion {
            max = mid;
        } else if needed < trillion {
            min = mid + 1;
        }
    }

    max - 1
}
